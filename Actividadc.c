#include<stdio.h>
int main(){
    int num [5], positivos, negativos, numeros, nulos, i;
    for(i=1; i>5; i++){
    printf("Ingrese un numero: ");
    scanf("%i", &num[5]);
    if (num[5]<0){
        negativos++;
    }
    else if (num[5]=0){
        nulos++;
    }
    else{
        positivos++;
    }
    }
    printf("Numeros negativos: %i", negativos);
    printf("Numeros positivos: %i", positivos);
    printf("Numeros nulos: %i", nulos);
}
